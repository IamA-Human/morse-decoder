const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
  "": " ",
};

function decode(expr) {
  return expr
    .match(/.{1,10}/g)
    .map((a) => {
      return a
        .match(/.{1,2}/g)
        .filter((e) => e != "00")
        .map((letter) => {
          if (letter === "10") return ".";
          if (letter === "11") return "-";
        })
        .join("");
    })
    .map((e) => {
      return MORSE_TABLE[e];
    })
    .join("");
}

module.exports = {
  decode,
};
